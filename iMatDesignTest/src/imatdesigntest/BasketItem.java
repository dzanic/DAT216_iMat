    private void incButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_incButtonActionPerformed
        this.shoppingItem.setAmount( this.shoppingItem.getAmount()  + 1);
        this.quantityField.setText(String.valueOf(this.shoppingItem.getAmount()));
        
        if(this.shoppingItem.getAmount() >= 2){
            this.decButton.setEnabled(true);
        }
    }//GEN-LAST:event_incButtonActionPerformed

    private void decButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_decButtonActionPerformed
       if (this.shoppingItem.getAmount() <= 2){
           this.shoppingItem.setAmount(this.shoppingItem.getAmount() - 1);
           this.quantityField.setText(String.valueOf(this.shoppingItem.getAmount()));
           this.decButton.setEnabled(false);
           System.out.println("the updated shopping item amount is: "+ this.shoppingItem.getAmount());
       } else
       { 
           this.shoppingItem.setAmount(this.shoppingItem.getAmount() - 1);
           this.quantityField.setText(String.valueOf(this.shoppingItem.getAmount()));
       }
    }//GEN-LAST:event_decButtonActionPerformed

    
