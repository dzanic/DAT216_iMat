    private void checkIfFavorite(){
       for (Product p : IMatDataHandler.getInstance().favorites()){
           if (p.equals(product)) {
               favoriteAddButton.setBackground(Color.ORANGE);
               this.favoriteAddButton.repaint();
           }
       }
    }
    private void spawnCannotAddDialog(){
        Timer timer = new Timer(2000, new ActionListener() {
            public void actionPerformed(ActionEvent e) {
                denyDialog.setVisible(false);
                denyDialog.dispose();
                
            }
        });
        denyDialog.setLocationRelativeTo(this.addToCartButton);
        denyDialog.setSize(160,36);
        timer.setRepeats(false);
        timer.start();
        denyDialog.setVisible(true);
        System.out.println("Dialog closed");
    }
    
    private void spawnAddedDialog(){
        Timer timer = new Timer(2000, new ActionListener() {
            public void actionPerformed(ActionEvent e) {
               addedDialog.setVisible(false);
               addedDialog.dispose();
                
            }
        });
        addedDialog.setLocationRelativeTo(this.addToCartButton);
        addedDialog.setSize(170,36);
        timer.setRepeats(false);
        timer.start();
        addedDialog.setVisible(true);
        System.out.println("Dialog closed");
    }
